        [Fact]
        public void GenerateApplicationClass_ContainsRevitGenRuntimeNamespace()
        {
            var classSymbol = GetClassSymbol(@"
using RevitGen.Attributes;
namespace MyAddin
{
    [RevitCommand(""Test"")]
    public partial class MyCmd
    {
