            var result = SourceGenerationHelper.GenerateApplicationClass(
                new[] { classSymbol });
            Assert.Contains("namespace RevitGen.Runtime", result);
        }

        [Fact]
        public void GenerateApplicationClass_MultipleCommandsInSamePanel_OneCreateRibbonPanelCall()
        {
            var source = @"
using RevitGen.Attributes;
namespace MyAddin
{
    [RevitCommand(""Cmd1"", TabName = ""MyTab"", PanelName = ""MyPanel"")]
    public partial class CmdA
    {
        [CommandHandler]
        private void Run() { }
    }

    [RevitCommand(""Cmd2"", TabName = ""MyTab"", PanelName = ""MyPanel"")]
    public partial class CmdB
    {
        [CommandHandler]
        private void Run() { }
    }
}";
            var parseOptions = CSharpParseOptions.Default.WithLanguageVersion(LanguageVersion.CSharp9);
            var compilation = CSharpCompilation.Create(
                "TestAssembly",
                syntaxTrees: new[]
                {
                    CSharpSyntaxTree.ParseText(AttributeSource, parseOptions),
                    CSharpSyntaxTree.ParseText(source, parseOptions)
                },
                references: new[]
                {
                    MetadataReference.CreateFromFile(typeof(object).Assembly.Location),
                    MetadataReference.CreateFromFile(Assembly.Load("System.Runtime").Location)
                },
                options: new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

            var symbols = compilation.GlobalNamespace
                .GetNamespaceMembers()
                .SelectMany(GetAllTypes)
                .Where(t => t.GetAttributes().Any(a =>
                    a.AttributeClass?.ToDisplayString() == "RevitGen.Attributes.RevitCommandAttribute"))
                .ToList();

            var result = SourceGenerationHelper.GenerateApplicationClass(symbols);

            // Both commands reference the same panel variable
            int panelCreations = System.Text.RegularExpressions.Regex
                .Matches(result, "CreateRibbonPanel").Count;
            Assert.Equal(1, panelCreations);
        }
    }
}
